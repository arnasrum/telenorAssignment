# Grid Logic: 
# We have 400 items (20x20). We need 10,000 items (100x100).
# We will repeat the 20x20 grid 5 times horizontally and 5 times vertically.

final_grid = []

row = 500
col = 500
# Generate the 100x100 grid
for row_idx in range(500):
    current_row = []
    for col_idx in range(500):
        # Map the current 100x100 index back to the 20x20 data source
        source_idx = (row_idx % 20) * 20 + (col_idx % 20)
        current_row.append(data[source_idx])
    final_grid.append(current_row)

# Print first 5 rows to verify, then save to file
print("Grid Dimension: {}x{}".format(len(final_grid), len(final_grid[0])))
print("First row preview:", final_grid[0][:10], "...")

# Write to file
with open(f"{row}x{col}_grid.txt", "w") as f:
    for row in final_grid:
        # Format as 4-digit zero-padded strings separated by spaces
        line = " ".join("{:04d}".format(num) for num in row)
        f.write(line + "\n")

print(f"File '{row}x{col}_grid.txt' has been generated.")
